#include <iostream>
#include <cmath> 

using namespace std;

int main (){
  float n = 6.5;
  cout << "round to the nearest integer " << round(n) << endl;
  cout << "round up " << ceil(n) << endl;
  cout << "round down " << floor(n) << endl;
  cout << "The value of n is " << n << endl;
  
return 0;
}
